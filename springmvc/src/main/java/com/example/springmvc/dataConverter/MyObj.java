package com.example.springmvc.dataConverter;

public class MyObj {
    String a;
    String b;

    public MyObj() {
    }

    public MyObj(String a, String b) {
        this.a = a;
        this.b = b;
    }

    public String getA() {
        return a;
    }

    public void setA(String a) {
        this.a = a;
    }

    public String getB() {
        return b;
    }

    public void setB(String b) {
        this.b = b;
    }
}
